package hr.fer.zemris.optjava.dz6;
/**
 * Interface that represents simple Cartesian point.
 * @author Branko
 *
 */
public interface CartesianPoint {
	/**
	 * Public getter which returns x coordinate of city
	 * 
	 * @return x coordinate of city
	 */
    double getX();
    /**
	 * Public getter which returns y coordinate of city
	 * 
	 * @return y coordinate of city
	 */
    double getY();
}